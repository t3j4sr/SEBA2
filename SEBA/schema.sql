-- ── Users table (for username/password auth) ──────────────────────────────────
-- Run in Supabase SQL Editor → New Query
CREATE TABLE IF NOT EXISTS public.seba_users (
    id            TEXT         PRIMARY KEY,   -- UUID string generated by backend
    username      TEXT         NOT NULL UNIQUE,
    password_hash TEXT         NOT NULL,
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_seba_users_username ON public.seba_users (username);

