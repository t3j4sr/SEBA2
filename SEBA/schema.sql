-- Transactions table
CREATE TABLE IF NOT EXISTS public.transactions (
    id              BIGSERIAL PRIMARY KEY,
    user_id         TEXT         NOT NULL,          -- Supabase Auth UUID (or demo UUID)
    amount          NUMERIC(12, 2) NOT NULL,
    date            DATE         NOT NULL,
    merchant        TEXT         NOT NULL,
    category        TEXT         NOT NULL DEFAULT 'Misc',
    is_impulsive    BOOLEAN      NOT NULL DEFAULT FALSE,
    impulse_reason  TEXT,                           -- Human-readable reason for the flag
    source          TEXT         NOT NULL DEFAULT 'sms',  -- 'sms' | 'statement' | 'json'
    raw_text        TEXT,
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);

-- Index for fast per-user queries (used by all endpoints)
CREATE INDEX IF NOT EXISTS idx_transactions_user_id
    ON public.transactions (user_id);

-- Index for date-range filtering
CREATE INDEX IF NOT EXISTS idx_transactions_date
    ON public.transactions (date DESC);

-- Index for category filtering
CREATE INDEX IF NOT EXISTS idx_transactions_category
    ON public.transactions (category);

